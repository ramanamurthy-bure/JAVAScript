// Conditional Statements
// 1. if statement
var marks = 85;
if(marks>80){
    console.log("Grade A");
}

// 2. if else statement
var marks = 75;
if(marks>80){
    console.log("Grade A");
}else
{
    console.log("Grade B");
}


// 3. nested if
var marks = 40;
if(marks>80){
    console.log("Grade A");
}
else if(marks>55)
{
    console.log("Grade B");
}
else{
    console.log("Grade C");
}


